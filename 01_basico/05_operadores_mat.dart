/**
 * Operadores matemáticos en dart
 * Tarea matemática o lógica,
 * que produce un resultado que se puede evaluar.
 */

void main(List<String> args) {
  /// Operador de suma
  int a = 10 + 5; // 15

  /// Operador de resta.
  int b = 15-2;

  // Operador de multiplicación.
  int c = 2*5;

  // Operador de división.
  double d = 10.0/2;

  // Dvisión sintética. Resto de la división.
  double e = 20.0%3;

  // Operador de negación. Es usado para cambiar el valor de la expresión.
  a = -a;

  // División. División entera, comun y corriente sin decimales.
  int f = 10 ~/3;

  // Operador de incremento y decremento.

  int g = 1;
  g++; // 1 en 1.
  g--;

  g+=2; // Operación y asignación.
  g-=1;


}